def fetch_page(url):
    headers = {
        'User-Agent': 'Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/91.0.4472.124 Safari/537.36',
        'Accept': 'text/html,application/xhtml+xml,application/xml;q=0.9,image/webp,image/apng,*/*;q=0.8',
        'Accept-Language': 'en-US,en;q=0.9'
    }
    response = requests.get(url, headers=headers)
    if response.status_code == 200:
        return response.text
    else:
        print(f"Failed to fetch the page: {url} with status code: {response.status_code}")
        return None

def extract_audio_and_sentences(content):
    pattern = r'<dt><script>l\((\d+),0\)</script>(.*?)\n'
    matches = re.findall(pattern, content, re.DOTALL)
    audio_and_sentences = []
    
    for match in matches:
        audio_number = match[0]
        sentence = re.sub(r'<.*?>', '', match[1]).strip()
        audio_url = f"http://study.aitech.ac.jp/tat/{audio_number}.mp3"
        audio_and_sentences.append({"sentence": sentence, "audio": audio_url})
    
    return audio_and_sentences

def save_to_json(data, filename='audio_sentences.json'):
    with open(filename, 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=4)

